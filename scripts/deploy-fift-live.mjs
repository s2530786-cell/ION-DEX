#!/usr/bin/env node
/**
 * Live deploy preflight: read secrets/env, require explicit human confirm, emit fift preflight.
 * Does NOT broadcast on-chain messages (skeleton). Set ION_DEPLOY_ALLOW_LIVE=1.
 */

import { createInterface } from "node:readline/promises";
import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { stdin as input, stdout as output } from "node:process";
import { join } from "node:path";

const root = process.cwd();
const ionRoot = join(root, "contracts", "ion");
const deployDir = join(ionRoot, "deploy");
const generatedFif = join(deployDir, "deploy-live.generated.fif");

const REQUIRED_ENVS = [
  "ION_DEPLOY_OWNER_ADDRESS",
  "ION_DEPLOY_LP_RECIPIENT",
  "ION_DEPLOY_TREASURY_RECIPIENT",
  "ION_DEPLOY_INSURANCE_RECIPIENT",
  "ION_DEPLOY_TOKEN0_ADDRESS",
  "ION_DEPLOY_TOKEN1_ADDRESS",
];

const OPTIONAL_ENVS = [
  "ION_DEPLOY_FEE_DISTRIBUTOR_ADDRESS",
  "ION_DEPLOY_ROUTER_ADDRESS",
  "ION_DEPLOY_VAULT_ADDRESS",
  "ION_DEPLOY_STAKING_POOL_ADDRESS",
  "ION_DEPLOY_SANDWICH_GUARD_ADDRESS",
  "ION_DEPLOY_RPC_URL",
];

function maskAddress(value) {
  if (value.length <= 16) {
    return value;
  }
  return `${value.slice(0, 8)}…${value.slice(-6)}`;
}

function assertAddress(name, value) {
  const trimmed = value.trim();
  if (trimmed.length < 48) {
    throw new Error(`${name} looks too short (${trimmed.length} chars)`);
  }
  if (trimmed.includes("<") || trimmed.includes(">")) {
    throw new Error(`${name} still contains placeholder syntax`);
  }
  if (/\s/.test(trimmed)) {
    throw new Error(`${name} must not contain whitespace`);
  }
  return trimmed;
}

function pickFift() {
  const candidates = [
    process.env.ION_FIFT_EXE,
    process.env.ION_TOOLCHAIN_ROOT
      ? join(process.env.ION_TOOLCHAIN_ROOT, "bin", "fift")
      : null,
  ].filter(Boolean);
  for (const candidate of candidates) {
    if (existsSync(candidate)) {
      return candidate;
    }
  }
  const probe = spawnSync(process.platform === "win32" ? "where" : "which", ["fift"], {
    encoding: "utf8",
    shell: true,
  });
  if (probe.status === 0 && probe.stdout?.trim()) {
    return "fift";
  }
  return null;
}

function buildGeneratedFif(config) {
  const lines = [
    "#!/usr/bin/env fift -s",
    "// AUTO-GENERATED by scripts/deploy-fift-live.mjs — do not commit secrets.",
    "",
    '"Fift.fif" include',
    "",
    '."=== ION DEX LIVE DEPLOY PREFLIGHT (skeleton) ===" cr',
    `."Network: ${config.network}" cr`,
    `."RPC: ${config.rpc}" cr`,
    `."Owner: ${config.owner}" cr`,
    `."LP recipient: ${config.lp}" cr`,
    `."Treasury recipient: ${config.treasury}" cr`,
    `."Insurance recipient: ${config.insurance}" cr`,
    `."Token0: ${config.token0}" cr`,
    `."Token1: ${config.token1}" cr`,
    `."FeeDistributor (post-deploy): ${config.feeDistributor}" cr`,
    `."Router (post-deploy): ${config.router}" cr`,
    `."Vault (post-deploy): ${config.vault}" cr`,
    `."Staking pool (post-deploy): ${config.stakingPool}" cr`,
    `."Sandwich guard (post-deploy): ${config.sandwichGuard}" cr`,
    '"" cr',
    '."Fee split: LP 50% | Staking 15% | Treasury 10% | Developer 25%" cr',
    '."Swap fee: 0.3% (fee_num=9970 / fee_denom=10000)" cr',
    '."Protocol fee: 5 bps" cr',
    '"" cr',
    '."SKIPPED: no sendRawMessage / wallet signing in preflight." cr',
    '."Next after audit: scripts/deploy-fift-live-send.mjs (plan or send mode)" cr',
  ];
  return `${lines.join("\n")}\n`;
}

async function requireHumanConfirm(network) {
  const expectedPhrase = `YES I deploy to ${network}`;
  const fromEnv = process.env.ION_DEPLOY_CONFIRM?.trim();

  if (fromEnv === expectedPhrase) {
    console.log(`OK human confirm via ION_DEPLOY_CONFIRM`);
    return;
  }

  if (!input.isTTY) {
    throw new Error(
      `Non-interactive shell: set ION_DEPLOY_CONFIRM="${expectedPhrase}" after manual review.`,
    );
  }

  console.log("");
  console.log("=== HUMAN CONFIRMATION REQUIRED ===");
  console.log(`Type exactly: ${expectedPhrase}`);
  const rl = createInterface({ input, output });
  const answer = (await rl.question("Confirm: ")).trim();
  rl.close();

  if (answer !== expectedPhrase) {
    throw new Error("Confirmation phrase mismatch — live preflight aborted.");
  }
  console.log("OK operator confirmed live preflight");
}

console.log("=== ION DEX live deploy preflight (skeleton) ===");

if (process.env.ION_DEPLOY_DRY_RUN === "1") {
  throw new Error("ION_DEPLOY_DRY_RUN=1 — use deploy-fift-dry-run.mjs instead.");
}

if (process.env.ION_DEPLOY_ALLOW_LIVE !== "1") {
  throw new Error(
    "Set ION_DEPLOY_ALLOW_LIVE=1 to run live preflight (still no on-chain broadcast in skeleton).",
  );
}

const network = process.env.ION_DEPLOY_NETWORK ?? "testnet";
if (network !== "testnet" && network !== "mainnet") {
  throw new Error(`ION_DEPLOY_NETWORK must be testnet or mainnet, got: ${network}`);
}

const config = {
  network,
  rpc:
    process.env.ION_DEPLOY_RPC_URL?.trim() ||
    (network === "mainnet"
      ? "https://api.mainnet.ice.io/http/v2/jsonRPC"
      : "https://api.testnet.ice.io/http/v2/jsonRPC"),
  owner: assertAddress("ION_DEPLOY_OWNER_ADDRESS", process.env.ION_DEPLOY_OWNER_ADDRESS ?? ""),
  lp: assertAddress("ION_DEPLOY_LP_RECIPIENT", process.env.ION_DEPLOY_LP_RECIPIENT ?? ""),
  treasury: assertAddress(
    "ION_DEPLOY_TREASURY_RECIPIENT",
    process.env.ION_DEPLOY_TREASURY_RECIPIENT ?? "",
  ),
  insurance: assertAddress(
    "ION_DEPLOY_INSURANCE_RECIPIENT",
    process.env.ION_DEPLOY_INSURANCE_RECIPIENT ?? "",
  ),
  token0: assertAddress("ION_DEPLOY_TOKEN0_ADDRESS", process.env.ION_DEPLOY_TOKEN0_ADDRESS ?? ""),
  token1: assertAddress("ION_DEPLOY_TOKEN1_ADDRESS", process.env.ION_DEPLOY_TOKEN1_ADDRESS ?? ""),
  feeDistributor: process.env.ION_DEPLOY_FEE_DISTRIBUTOR_ADDRESS?.trim() || "(deploy in step 1)",
  router: process.env.ION_DEPLOY_ROUTER_ADDRESS?.trim() || "(deploy in step 2)",
  vault: process.env.ION_DEPLOY_VAULT_ADDRESS?.trim() || "(deploy in step 3)",
  stakingPool: process.env.ION_DEPLOY_STAKING_POOL_ADDRESS?.trim() || "(deploy in step 4)",
  sandwichGuard: process.env.ION_DEPLOY_SANDWICH_GUARD_ADDRESS?.trim() || "(deploy optional)",
};

for (const key of REQUIRED_ENVS) {
  if (!process.env[key]?.trim()) {
    throw new Error(`Missing required env: ${key}`);
  }
}

console.log("Resolved configuration (masked):");
console.log(`  network=${config.network}`);
console.log(`  rpc=${config.rpc}`);
for (const [label, value] of [
  ["owner", config.owner],
  ["lp", config.lp],
  ["treasury", config.treasury],
  ["insurance", config.insurance],
  ["token0", config.token0],
  ["token1", config.token1],
]) {
  console.log(`  ${label}=${maskAddress(value)}`);
}

for (const key of OPTIONAL_ENVS) {
  const value = process.env[key]?.trim();
  console.log(value ? `  ${key}=${maskAddress(value)}` : `  ${key}=(unset)`);
}

if (process.env.ION_DEPLOY_KEY?.trim()) {
  console.log("  ION_DEPLOY_KEY=(set, not used by skeleton)");
} else {
  console.log("WARN ION_DEPLOY_KEY unset — required later for real broadcast step");
}

await requireHumanConfirm(network);

mkdirSync(deployDir, { recursive: true });
writeFileSync(generatedFif, buildGeneratedFif(config), "utf8");
console.log(`OK wrote ${generatedFif}`);

const fiftExe = pickFift();
if (!fiftExe) {
  console.log("SKIP - fift binary not found; run generated script locally:");
  console.log(`  cd contracts/ion && fift -s deploy/deploy-live.generated.fif`);
  console.log("OK - live deploy preflight complete (no chain transactions sent)");
  console.log("RESULT=GREEN");
  process.exit(0);
}

const fiftLib =
  process.env.FIFTPATH ??
  (process.env.ION_TOOLCHAIN_ROOT
    ? join(process.env.ION_TOOLCHAIN_ROOT, "fift-lib")
    : "");

const run = spawnSync(fiftExe, ["-s", "deploy/deploy-live.generated.fif"], {
  cwd: ionRoot,
  encoding: "utf8",
  env: {
    ...process.env,
    FIFTPATH: fiftLib,
  },
});

if (run.stdout) {
  console.log(run.stdout.trim());
}
if (run.status !== 0) {
  if (run.stderr) {
    console.error(run.stderr.trim());
  }
  throw new Error("fift live preflight failed");
}

console.log("");
console.log("OK - live deploy preflight complete (no chain transactions sent)");
console.log("RESULT=GREEN");
