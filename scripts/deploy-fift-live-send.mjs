#!/usr/bin/env node
/**
 * Live broadcast segment: generate FeeDistributor StateInit + wallet-query BoC.
 * Never auto-submits to RPC unless operator runs lite-client/toncli manually.
 *
 * Modes:
 *   ION_DEPLOY_SEND_MODE=plan  (default) — StateInit BoC only
 *   ION_DEPLOY_SEND_MODE=send  — also invoke wallet-v3.fif (requires wallet files + seqno)
 */

import { createInterface } from "node:readline/promises";
import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { stdin as input, stdout as output } from "node:process";
import { join } from "node:path";
import {
  REQUIRED_DEPLOY_ENVS,
  assertRequiredEnvs,
  loadDeployConfig,
  logMaskedConfig,
  resolveDeployNetwork,
} from "./lib/ion-deploy-env.mjs";

const root = process.cwd();
const ionRoot = join(root, "contracts", "ion");
const deployDir = join(ionRoot, "deploy");
const outDir = join(deployDir, "out");
const paramsFif = join(deployDir, "deploy-live-params.generated.fif");
const sendFif = join(deployDir, "deploy-live-send.fif");
const feeDistBuild = join(ionRoot, "build", "FeeDistributor.fif");

function pickFift() {
  const candidates = [
    process.env.ION_FIFT_EXE,
    process.env.ION_TOOLCHAIN_ROOT
      ? join(process.env.ION_TOOLCHAIN_ROOT, "bin", "fift")
      : null,
  ].filter(Boolean);
  for (const candidate of candidates) {
    if (existsSync(candidate)) {
      return candidate;
    }
  }
  const probe = spawnSync(process.platform === "win32" ? "where" : "which", ["fift"], {
    encoding: "utf8",
    shell: true,
  });
  if (probe.status === 0 && probe.stdout?.trim()) {
    return "fift";
  }
  return null;
}

function resolveSmartcontDir() {
  const candidates = [
    process.env.ION_SMARTCONT_DIR,
    process.env.ION_SOURCE_ROOT
      ? join(process.env.ION_SOURCE_ROOT, "crypto", "smartcont")
      : null,
    "D:/openclaw-data/workspace/ion-official/ion/crypto/smartcont",
  ].filter(Boolean);
  for (const dir of candidates) {
    const walletV3 = join(dir, "wallet-v3.fif");
    if (existsSync(walletV3)) {
      return dir;
    }
  }
  return null;
}

function fiftEnv(fiftLib) {
  const smartcont = resolveSmartcontDir();
  const paths = [fiftLib, smartcont].filter(Boolean);
  return {
    ...process.env,
    FIFTPATH: paths.join(process.platform === "win32" ? ";" : ":"),
  };
}

function escapeFiftString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function buildParamsFif(config, { broadcastEnabled, sendMode }) {
  const totalBps = config.lpBps + config.treasuryBps + config.insuranceBps;
  if (totalBps !== 10000) {
    throw new Error(`Fee bps must sum to 10000, got ${totalBps}`);
  }

  return `#!/usr/bin/env fift -s
// AUTO-GENERATED by scripts/deploy-fift-live-send.mjs — do not commit.
${broadcastEnabled ? "true" : "false"} constant broadcast_enabled
"${escapeFiftString(config.network)}" constant network_name
"${escapeFiftString(config.owner)}" constant owner_addr_str
"${escapeFiftString(config.lp)}" constant lp_addr_str
"${escapeFiftString(config.treasury)}" constant treasury_addr_str
"${escapeFiftString(config.insurance)}" constant insurance_addr_str
${config.lpBps} constant lp_bps
${config.treasuryBps} constant treasury_bps
${config.insuranceBps} constant insurance_bps
"${escapeFiftString(config.walletBase)}" constant wallet_file_base
${config.walletSeqno} constant wallet_seqno
${config.walletSubwallet} constant wallet_subwallet_id
${config.deployGrams.toString()} constant deploy_grams
"${escapeFiftString(sendMode)}" constant send_mode_label
`;
}

async function requireBroadcastConfirm(network) {
  const expectedPhrase = `YES BROADCAST to ${network}`;
  const fromEnv = process.env.ION_DEPLOY_CONFIRM?.trim();
  if (fromEnv === expectedPhrase) {
    console.log("OK human confirm via ION_DEPLOY_CONFIRM");
    return;
  }
  if (!input.isTTY) {
    throw new Error(
      `Non-interactive: set ION_DEPLOY_CONFIRM="${expectedPhrase}" after audit sign-off.`,
    );
  }
  console.log("");
  console.log("=== BROADCAST CONFIRMATION (on-chain BoC) ===");
  console.log(`Type exactly: ${expectedPhrase}`);
  const rl = createInterface({ input, output });
  const answer = (await rl.question("Confirm: ")).trim();
  rl.close();
  if (answer !== expectedPhrase) {
    throw new Error("Broadcast confirmation mismatch — aborted.");
  }
}

function assertNotCi() {
  if (process.env.GITHUB_ACTIONS === "true" && process.env.ION_DEPLOY_FORCE_BROADCAST !== "1") {
    throw new Error(
      "Refusing broadcast on GitHub Actions. Run locally on a trusted machine after audit.",
    );
  }
  if (process.env.CI === "true" && process.env.ION_DEPLOY_FORCE_BROADCAST !== "1") {
    throw new Error("Refusing broadcast when CI=true.");
  }
}

console.log("=== ION DEX live deploy SEND ===");

assertNotCi();

if (process.env.ION_DEPLOY_BROADCAST !== "1") {
  throw new Error("Set ION_DEPLOY_BROADCAST=1 to run live send segment.");
}

const sendMode = (process.env.ION_DEPLOY_SEND_MODE ?? "plan").toLowerCase();
if (sendMode !== "plan" && sendMode !== "send") {
  throw new Error("ION_DEPLOY_SEND_MODE must be plan or send");
}

const network = resolveDeployNetwork();
assertRequiredEnvs();
const config = loadDeployConfig();

if (!existsSync(sendFif)) {
  throw new Error(`Missing ${sendFif}`);
}
if (!existsSync(feeDistBuild)) {
  throw new Error(
    `Missing ${feeDistBuild} — compile FeeDistributor.fc with func before broadcast.`,
  );
}

logMaskedConfig(config);
console.log(`  send_mode=${sendMode}`);
console.log(`  lp_bps=${config.lpBps} treasury_bps=${config.treasuryBps} insurance_bps=${config.insuranceBps}`);
console.log(`  deploy_grams=${config.deployGrams}`);

if (sendMode === "send") {
  if (!config.walletBase) {
    throw new Error("ION_DEPLOY_WALLET_BASE required for send mode (wallet .pk/.addr base path)");
  }
  if (!Number.isInteger(config.walletSeqno) || config.walletSeqno < 0) {
    throw new Error("ION_DEPLOY_WALLET_SEQNO must be a non-negative integer");
  }
  const pkPath = `${config.walletBase}.pk`;
  const addrPath = `${config.walletBase}.addr`;
  if (!existsSync(pkPath) || !existsSync(addrPath)) {
    throw new Error(`Wallet files missing: ${pkPath} and/or ${addrPath}`);
  }
  console.log(`  wallet_base=${config.walletBase}`);
  console.log(`  wallet_seqno=${config.walletSeqno}`);
}

await requireBroadcastConfirm(network);

const broadcastEnabled = sendMode === "send";
mkdirSync(outDir, { recursive: true });
writeFileSync(paramsFif, buildParamsFif(config, { broadcastEnabled, sendMode }), "utf8");
console.log(`OK wrote ${paramsFif}`);

const fiftExe = pickFift();
if (!fiftExe) {
  console.log("SKIP - fift not found; after installing toolchain:");
  console.log("  cd contracts/ion && fift -s deploy/deploy-live-send.fif");
  console.log("RESULT=GREEN");
  process.exit(0);
}

const fiftLib =
  process.env.FIFTPATH ??
  (process.env.ION_TOOLCHAIN_ROOT
    ? join(process.env.ION_TOOLCHAIN_ROOT, "fift-lib")
    : "");

const runSend = spawnSync(fiftExe, ["-s", "deploy/deploy-live-send.fif"], {
  cwd: ionRoot,
  encoding: "utf8",
  env: fiftEnv(fiftLib),
});

if (runSend.stdout) {
  console.log(runSend.stdout.trim());
}
if (runSend.status !== 0) {
  if (runSend.stderr) {
    console.error(runSend.stderr.trim());
  }
  throw new Error("deploy-live-send.fif failed");
}

const stateInitBoc = join(outDir, "fee-distributor-state-init.boc");
if (!existsSync(stateInitBoc)) {
  throw new Error(`Expected ${stateInitBoc} after fift send script`);
}
console.log(`OK ${stateInitBoc}`);

if (sendMode === "send") {
  const smartcont = resolveSmartcontDir();
  if (!smartcont) {
    throw new Error(
      "ION smartcont dir not found (wallet-v3.fif). Set ION_SMARTCONT_DIR or clone ion-official.",
    );
  }
  const walletV3 = join(smartcont, "wallet-v3.fif");
  const initRel = "deploy/out/fee-distributor-state-init.boc";
  const queryOut = join(outDir, "fee-distributor-wallet-query.boc");
  const amountTon = `${config.deployGrams}`;

  // Recompute contract address via short fift snippet is done in send.fif; wallet dest uses file from operator env
  const fromStdout = runSend.stdout?.match(
    /FeeDistributor contract address \(bounceable\): (\S+)/,
  )?.[1];
  const destAddr =
    fromStdout ||
    process.env.ION_DEPLOY_FEE_DISTRIBUTOR_DEST?.trim() ||
    process.env.ION_DEPLOY_FEE_DISTRIBUTOR_ADDRESS?.trim();
  if (!destAddr) {
    console.log("");
    console.log("WARN ION_DEPLOY_FEE_DISTRIBUTOR_DEST unset.");
    console.log("Read FeeDistributor address from fift output above, then run:");
    console.log(
      `  fift -s ${walletV3} ${config.walletBase} <dest-addr> ${config.walletSubwallet} ${config.walletSeqno} ${amountTon} -I ${initRel} ${queryOut}`,
    );
    console.log("RESULT=GREEN");
    process.exit(0);
  }

  const runWallet = spawnSync(
    fiftExe,
    [
      "-s",
      walletV3,
      config.walletBase,
      destAddr,
      String(config.walletSubwallet),
      String(config.walletSeqno),
      amountTon,
      "-I",
      initRel,
      queryOut.replace(/\.boc$/, ""),
    ],
    {
      cwd: ionRoot,
      encoding: "utf8",
      env: fiftEnv(fiftLib),
    },
  );

  if (runWallet.stdout) {
    console.log(runWallet.stdout.trim());
  }
  if (runWallet.status !== 0) {
    if (runWallet.stderr) {
      console.error(runWallet.stderr.trim());
    }
    throw new Error("wallet-v3.fif failed");
  }
  if (!existsSync(queryOut)) {
    throw new Error(`Expected wallet query BoC: ${queryOut}`);
  }
  console.log(`OK ${queryOut}`);
  console.log("Submit wallet-query.boc via lite-client / tonlib / operator RPC tool.");
}

console.log("");
console.log("OK - live send segment complete (no automatic RPC submit)");
console.log("RESULT=GREEN");
